	Resource::WP  Resource::instance;
	//-------------------------------------------------------------------
	//リソースの初期化
	bool  Resource::Initialize()
	{
		this->img = DG::Image::Create("./data/image/BeforePlay/BeforePlay.png");
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		this->img.reset();
		return true;
	}
	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, true);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		//画像優先度
		this->render2D_Priority[1] = 0.2f;
		//画像サイズ
		this->SetImgSize(960, 540);
		//矩形
		this->drawBase = ML::Box2D(0, 0, this->imgSizeW, this->imgSizeH);
		this->srcBase = ML::Box2D(0, 0, this->imgSizeW, this->imgSizeH);
		//フラグ
		this->createdPlay = false;
		//生存時間
		this->taskLife = 100;
		//ファイルパス
		this->filePath = "";
		//★タスクの生成

		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		//★データ＆タスク解放


		if (!ge->QuitFlag() && this->nextTaskCreate) {
			//★引き継ぎタスクの生成
		}

		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		//時間になったらゲーム生成処理
		if (true == ge->finishedLoadDisappear) {
			if (this->filePath != "") {
				if (true == this->createdPlay) {
					if (this->taskLife == 0) {
						this->Kill();
					}

					--this->taskLife;
				}
				else {
					ge->stageFilePath = this->filePath;
					Play::Object::Create(true);
					this->createdPlay = true;
				}
			}

		}
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Color color = ML::Color(
			0.01f * this->taskLife,
			1,
			1,
			1
		);

		this->res->img->Draw(this->drawBase, this->srcBase, color);
	}

