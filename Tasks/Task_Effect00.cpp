	//-------------------------------------------------------------------
	//リソースの初期化
	bool  Resource::Initialize()
	{
		this->img = DG::Image::Create("./data/image/Effect/星-01.png");
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		this->img.reset();
		return true;
	}
	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, false);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		//画像優先度
		this->render2D_Priority[1] = 0.6f;
		//位置情報
		this->pos = ML::Vec2(0, 0);
		//角度
		this->angle = ML::ToRadian((float)(rand() % 360));
		//移動情報
		this->moveVec = ML::Vec2(cos(angle) * 4, sin(angle) * 4);
		//生存時間
		this->life = 1.0f;
		//★タスクの生成

		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		//★データ＆タスク解放


		if (!ge->QuitFlag() && this->nextTaskCreate) {
			//★引き継ぎタスクの生成
		}

		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		//生存時間減らす
		this->life -= 0.01f;
		//位置移動
		this->pos += moveVec;
		//重力風処理
		this->moveVec.y += 0.08f;
		if (this->life < 0) {
			this->Kill();
		}
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D draw(-32, -32, 64, 64);
		draw.Offset(this->pos);
		draw.Offset(-ge->camera2D.x, -ge->camera2D.y);
		ML::Box2D src(0, 0, 256, 256);
		this->res->img->Rotation(this->angle, ML::Vec2(draw.w / 2.0f, draw.h / 2.0f));		//二つ目の引数は回転の軸部分
		this->res->img->Draw(draw, src, ML::Color(this->life, 1, 1, 1));
	}

