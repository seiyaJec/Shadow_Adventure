	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		if (this->life <= 0) {
			this->Kill();
		}
		--this->life;
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D draw = this->drawBase.OffsetCopy(this->pos);
		ML::Box2D src = this->srcBase;
		float alpha = this->life / (float)this->lifeMax;
		this->res->img->Draw(draw, src, ML::Color(alpha, 1, 1, 1));
	}

	//-------------------------------------------------------------------
	//関数定義
	//-------------------------------------------------------------------
	//画像サイズ、生存時間変更
	void Object::Set(int sizeW_, int sizeH_, int lifeMax_)
	{
		//画像サイズ
		this->SetImgSize(sizeW_, sizeH_);
		//矩形
		this->drawBase = setCenterPoint(sizeW_, sizeH_);
		//生存時間
		this->lifeMax = lifeMax_;
		this->life = lifeMax_;
	}

