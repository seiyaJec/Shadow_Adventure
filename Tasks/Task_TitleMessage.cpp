	//-------------------------------------------------------------------
	//リソースの初期化
	bool  Resource::Initialize()
	{
		this->img = DG::Image::Create("./data/image/Title/TitleMessage.png");
		return true;
	}
	//-------------------------------------------------------------------
	//リソースの解放
	bool  Resource::Finalize()
	{
		this->img.reset();
		return true;
	}
	//-------------------------------------------------------------------
	//「初期化」タスク生成時に１回だけ行う処理
	bool  Object::Initialize()
	{
		//スーパークラス初期化
		__super::Initialize(defGroupName, defName, true);
		//リソースクラス生成orリソース共有
		this->res = Resource::Create();

		//★データ初期化
		//画像優先度
		this->render2D_Priority[1] = 0.3f;
		//画像サイズ
		this->SetImgSize(358, 47);
		//位置情報
		this->pos = ML::Vec2(ge->screen2DWidth / 2.0f, 350);
		//矩形
		this->drawBase = setCenterPoint(this->imgSizeW, this->imgSizeH);
		this->srcBase = ML::Box2D(0, 0, this->imgSizeW, this->imgSizeH);
		//アニメーション
		this->animCnt = 0;
		this->animDistance = 40;
		//★タスクの生成

		return  true;
	}
	//-------------------------------------------------------------------
	//「終了」タスク消滅時に１回だけ行う処理
	bool  Object::Finalize()
	{
		//★データ＆タスク解放


		if (!ge->QuitFlag() && this->nextTaskCreate) {
			//★引き継ぎタスクの生成
		}

		return  true;
	}
	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		++animCnt;
		//点滅処理
		if (this->animCnt >= this->animDistance) {
			this->zeroAlpha = !this->zeroAlpha;
			this->animCnt = 0;
		}
	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		float alpha = 1;
		if (true == this->zeroAlpha) {
			alpha = 0;
		}
		ML::Box2D draw = this->drawBase.OffsetCopy(this->pos);
		this->res->img->Draw(draw, this->srcBase, ML::Color(alpha, 1, 1, 1));
	}

